                            <p>FROM ODH: Old Dog Haven is a foster-based rescue for senior dogs in Western Washington. We take in dogs 8 years and older, place them in safe, loving homes and provide all veterinary care necessary to restore the dog to health and an optimal quality of life. Priority is given to pulling dogs from shelters and those in hospice condition, but we also assist owners and shelters with finding appropriate homes for their adoptable senior dogs. The majority of ODH dogs are not adoptable and will live with their foster families for life as Final Refuge fosters. We currently have around 300 dogs in care and veterinary expenses run approximately $80K per month. Helping them is sometimes challenging, often expensive, but always rewarding. Our goal is that their last years are happy and that they die safe and at peace, knowing they are loved.</p>

